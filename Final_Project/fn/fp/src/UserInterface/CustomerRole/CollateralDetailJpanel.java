    private void txtValuationKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValuationKeyPressed
        // TODO add your handling code here:
        int event =evt.getKeyCode();
        if((event>=evt.VK_0 && event<=evt.VK_9) || event ==evt.VK_BACK_SPACE || (event>=evt.VK_NUMPAD0 && event<=evt.VK_NUMPAD9))
        {
            txtValuation.setEditable(true);
            
        }
        else
        {
            txtValuation.setEditable(false);
            JOptionPane.showMessageDialog(null, "Please enter valid valuation price","Warning",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_txtValuationKeyPressed

