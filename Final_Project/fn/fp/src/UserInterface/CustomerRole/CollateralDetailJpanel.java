    private void colltypeComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_colltypeComboBoxActionPerformed
        // TODO add your handling code here:
         String colltype=(String) colltypeComboBox.getSelectedItem();
         if(colltype.equalsIgnoreCase("Negotiable"))
         {
             hazardsComboBox.setEnabled(false);
             txtdescription.setEnabled(false);
             txtdescription.setText("NA");
         }
         else if(colltype.equalsIgnoreCase("Non-negotiable"))
         {
             hazardsComboBox.setEnabled(false);
             txtdescription.setEnabled(false);
             txtdescription.setText("NA");
             txtInsuranceAmount.setText("0.0");
             txtInsuranceAmount.setEnabled(false);
         }
         else if(colltype.equalsIgnoreCase("Real-Estate"))
         {
             hazardsComboBox.setEnabled(true);
             txtdescription.setEnabled(true);
            // txtdescription.setText("");
             txtInsuranceAmount.setEnabled(true);
             
         }
    }//GEN-LAST:event_colltypeComboBoxActionPerformed


