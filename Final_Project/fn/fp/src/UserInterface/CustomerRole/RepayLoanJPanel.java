        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please the row","Warning",JOptionPane.WARNING_MESSAGE);
            return;
            
        }

        Loan loan = (Loan)workRequestJTable.getValueAt(selectedRow, 0);
        txtLoanId.setText(String.valueOf(loan.getLoanId()));
        txtLoanAmount.setText(String.valueOf(loan.getLoanAmount()));
        
        txtInstallamount.setText(String.valueOf(loan.getLoanAmount()*0.13));
        txtNoOfInstall.setText(String.valueOf(loan.getNoOfInstallments()));
        txtNoOfInstallpaid.setText(String.valueOf(loan.getNoOfInstallmentsPaid()));
        txtnoOfInstallUnpaid.setText(String.valueOf(loan.getNoOfInstallmentsUnpaid()));
        txtLoanId.setEnabled(false);
        txtStartDate.setText(String.valueOf(loan.getRepaymentStartDate()));
        txtEndDate.setText(String.valueOf(loan.getRepaymentEndDAte()));
