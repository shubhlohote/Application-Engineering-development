    private void btnRepayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRepayActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();
        Loan loan = (Loan)workRequestJTable.getValueAt(selectedRow, 0);
        double previousLoanAmount=loan.getLoanAmount();
        double installAmt=Double.parseDouble(txtInstallamount.getText());
        int paid= Integer.parseInt(txtNoOfInstallpaid.getText());
        int total=Integer.parseInt(txtNoOfInstall.getText());
        int unpaid =Integer.parseInt(txtnoOfInstallUnpaid.getText());
        double repayedamt=0.0;
        
        if(loan.getAmortization().equalsIgnoreCase("Yes") && (paid!=total))
        {
            previousLoanAmount=loan.getLoanAmount();
            //loan.setLoanAmount(previousLoanAmount-(0.6*installAmt));repayedamt=repayedamt+installAmt;
            //txtLoanAmount.setText(String.valueOf(previousLoanAmount-(0.6*installAmt)));
            installAmt=Double.parseDouble(txtInstallamount.getText());
            
            loan.setNoOfInstallmentsPaid(paid+1);
            txtNoOfInstallpaid.setText(String.valueOf(paid +1));
            loan.setNoOfInstallmentsUnpaid(total-loan.getNoOfInstallmentsPaid());
            txtnoOfInstallUnpaid.setText(String.valueOf(total-loan.getNoOfInstallmentsPaid()));
            //loan.setNoOfInstallments(FRAMEBITS);
            
           JOptionPane.showMessageDialog(null, "Installment repayed successfully","Information",JOptionPane.INFORMATION_MESSAGE);
            
        }
        else if(loan.getAmortization().equalsIgnoreCase("No") && (paid!=total))
        {
            previousLoanAmount=loan.getLoanAmount();
            installAmt=Double.parseDouble(txtInstallamount.getText());
          
           // loan.setLoanAmount(previousLoanAmount-(0.2*installAmt));
           // txtLoanAmount.setText(String.valueOf(previousLoanAmount-(0.2*installAmt)));
            loan.setNoOfInstallmentsPaid(paid+1);
            txtNoOfInstallpaid.setText(String.valueOf(paid +1));
            loan.setNoOfInstallmentsUnpaid(total-loan.getNoOfInstallmentsPaid());
            txtnoOfInstallUnpaid.setText(String.valueOf(total-loan.getNoOfInstallmentsPaid()));
            
            
        }
        if (paid == total)
        {
            loan.setStatus("Repayed");
            JOptionPane.showMessageDialog(null, "All scheduled installments are paid...!Thank you","Information",JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnRepayActionPerformed

