    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : department.getWorkQueue().getWorkRequestList()){
            if(request instanceof RiskAssessmentWorkRequest){
            Object[] row = new Object[6];
            row[0] = request;
            System.out.println("sender" + request.getSender().getCustomer().getCustomerId() );
           // System.out.println("");
            row[1] = request.getSender().getCustomer().getSSN();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getFirstName();
            row[3]=((RiskAssessmentWorkRequest)request).getLoanAmount();
            row[4]=((RiskAssessmentWorkRequest)request).getLoanType();
            
            row[5] = request.getStatus();
            //row[4]= request.getSender().
            
            model.addRow(row);
            }
        }
    }

