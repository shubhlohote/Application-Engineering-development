    private void btnDisburseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisburseActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select the loan","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }

        RiskAssessmentWorkRequest request = (RiskAssessmentWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        request.setReceiver(userAccount);
        if(request.getLoanType().equalsIgnoreCase("Standalone"))
        {
         request.setStatus("Disbursed");  
      //   request.getLoan().setStatus("Disbursed");
        }
        else
        {
            for(int i=0;i<=request.getNoOfTakedown();i++)
            {
                if(i==request.getNoOfTakedown())
                {
                    request.setStatus("Disbursed");
                    break;
                  //  request.getLoan().setStatus("Disbursed");
                }
                request.setStatus("Disbursed Takedown" + i);
             //   request.getLoan().setStatus("Disbursed");
            }
        }
        
        
        
        
        populateTable();
        
    }//GEN-LAST:event_btnDisburseActionPerformed

